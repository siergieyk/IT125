DROP DATABASE IF EXISTS franks_frames;

CREATE DATABASE franks_frames;

CREATE TABLE franks_frames.Frame(
FrameId INT PRIMARY KEY AUTO_INCREMENT,
FrameName VARCHAR(45) NOT NULL,
FrameColor ENUM("Gold","Silver","Black","White") NOT NULL,
FrameWidth FLOAT(4,2) NOT NULL,
FramePricePLF DECIMAL(5,2) NOT NULL DEFAULT 0.00); 

CREATE TABLE franks_frames.Glass(
GlassId INT PRIMARY KEY AUTO_INCREMENT,
GlassStyle VARCHAR(45) NOT NULL,
GlassPricePSF DECIMAL(4,2) NOT NULL DEFAULT 0.00);

CREATE TABLE franks_frames.Customer(
CustomerId INT PRIMARY KEY AUTO_INCREMENT,
CustomerName VARCHAR(45) NOT NULL,
CustomerPhone CHAR(10));


CREATE TABLE franks_frames.Mat(
MatId INT PRIMARY KEY AUTO_INCREMENT,
MatColor ENUM("White","Gray","Black","Sand") NOT NULL,
MatPricePSF DECIMAL(4,2) NOT NULL DEFAULT 0.00); 

CREATE TABLE franks_frames.Poster(
PosterId INT PRIMARY KEY AUTO_INCREMENT,
PosterName VARCHAR(45) NOT NULL,
PosterPrice DECIMAL(5,2) NOT NULL DEFAULT 0.00,
PosterWidth FLOAT(5,2),
PosterHeight FLOAT(5,2));

CREATE TABLE franks_frames.Supplier(
SupplierId INT PRIMARY KEY AUTO_INCREMENT,
SupplierName VARCHAR(45) NOT NULL,
SupplierPhone CHAR(10) NOT NULL);


CREATE TABLE franks_frames.Order(
OrderId INT PRIMARY KEY AUTO_INCREMENT,
CustomerId INT NOT NULL,
PosterId INT NOT NULL,
MatId INT NOT NULL,
FrameId INT NOT NULL,
GlassId INT NOT NULL,
OrderPromised DATE NOT NULL,
OrderPrice DECIMAL(6,2) NOT NULL,

CONSTRAINT fk_CustomerId_CustomerO	FOREIGN KEY (CustomerId) 	REFERENCES Customer (CustomerId),
CONSTRAINT fk_PosterId_PosterO		FOREIGN KEY (PosterId) 		REFERENCES Poster (PosterId),
CONSTRAINT fk_MatId_MatO			FOREIGN KEY (MatId) 		REFERENCES Mat (MatId),
CONSTRAINT fk_FrameId_FrameO		FOREIGN KEY (FrameId) 		REFERENCES Frame (FrameId),
CONSTRAINT fk_GlassId_GlassO		FOREIGN KEY (GlassId) 		REFERENCES Glass (GlassId));


CREATE TABLE franks_frames.PosterSupplier(
PosterId INT NOT NULL,
SupplierId INT NOT NULL,

CONSTRAINT 								PRIMARY KEY (PosterId, SupplierId),
CONSTRAINT fk_PosterId_PosterPS			FOREIGN KEY (PosterId) 		REFERENCES Poster (PosterId),
CONSTRAINT fk_SupplierID_SupplierPS		FOREIGN KEY (SupplierId) 	REFERENCES Supplier (SupplierId));

